import 'dart:convert';
import 'package:http/http.dart' as http;
import '../models/task.dart';
import '../models/app_notification.dart';

/// Service class that handles all REST API communication with the Laravel backend.
///
/// API Base URL should point to your Laravel server.
/// For Android Emulator use: http://10.0.2.2:8000/api
/// For iOS Simulator / Physical device use your machine IP: http://YOUR_IP:8000/api
/// For web use: http://localhost:8000/api
class ApiService {
  // ── Change this URL to match your Laravel server address ──
  static const String _baseUrl = 'http://127.0.0.1:8000/api';

  /// Bearer token for authenticated requests (set after login).
  static String? _token;

  /// Set the auth token for all subsequent API calls.
  static void setToken(String? token) {
    _token = token;
  }

  /// Common headers with optional auth.
  Map<String, String> get _headers => {
    'Accept': 'application/json',
    if (_token != null) 'Authorization': 'Bearer $_token',
  };

  /// Common headers for JSON body requests.
  Map<String, String> get _jsonHeaders => {
    'Content-Type': 'application/json; charset=UTF-8',
    'Accept': 'application/json',
    if (_token != null) 'Authorization': 'Bearer $_token',
  };

  // ── READ ──────────────────────────────────────────────────────────────
  /// Fetch all tasks (GET /api/tasks)
  Future<List<Task>> fetchTasks() async {
    final response = await http.get(
      Uri.parse('$_baseUrl/tasks'),
      headers: _headers,
    );

    if (response.statusCode == 200) {
      final Map<String, dynamic> body = json.decode(response.body);
      final List<dynamic> data = body['data'] ?? [];
      return data.map((item) => Task.fromJson(item)).toList();
    } else {
      throw Exception('Failed to load tasks (${response.statusCode})');
    }
  }

  /// Fetch a single task by id (GET /api/tasks/:id)
  Future<Task> fetchTask(int id) async {
    final response = await http.get(
      Uri.parse('$_baseUrl/tasks/$id'),
      headers: _headers,
    );

    if (response.statusCode == 200) {
      final Map<String, dynamic> body = json.decode(response.body);
      return Task.fromJson(body['data']);
    } else {
      throw Exception('Failed to load task (${response.statusCode})');
    }
  }

  // ── CREATE ────────────────────────────────────────────────────────────
  /// Create a new task (POST /api/tasks)
  Future<Task> createTask(Task task) async {
    final response = await http.post(
      Uri.parse('$_baseUrl/tasks'),
      headers: _jsonHeaders,
      body: json.encode(task.toJson()),
    );

    if (response.statusCode == 201) {
      final Map<String, dynamic> body = json.decode(response.body);
      return Task.fromJson(body['data']);
    } else {
      throw Exception('Failed to create task (${response.statusCode})');
    }
  }

  // ── UPDATE ────────────────────────────────────────────────────────────
  /// Update an existing task (PUT /api/tasks/:id)
  Future<Task> updateTask(Task task) async {
    final response = await http.put(
      Uri.parse('$_baseUrl/tasks/${task.id}'),
      headers: _jsonHeaders,
      body: json.encode(task.toJson()),
    );

    if (response.statusCode == 200) {
      final Map<String, dynamic> body = json.decode(response.body);
      return Task.fromJson(body['data']);
    } else {
      throw Exception('Failed to update task (${response.statusCode})');
    }
  }

  // ── DELETE ────────────────────────────────────────────────────────────
  /// Delete a task (DELETE /api/tasks/:id)
  Future<bool> deleteTask(int id) async {
    final response = await http.delete(
      Uri.parse('$_baseUrl/tasks/$id'),
      headers: _headers,
    );

    if (response.statusCode == 200) {
      return true;
    } else {
      throw Exception('Failed to delete task (${response.statusCode})');
    }
  }

  // ════════════════════════════════════════════════════════════════════════
  // NOTIFICATIONS
  // ════════════════════════════════════════════════════════════════════════

  /// Fetch all notifications (GET /api/notifications)
  Future<List<AppNotification>> fetchNotifications() async {
    final response = await http.get(
      Uri.parse('$_baseUrl/notifications'),
      headers: _headers,
    );

    if (response.statusCode == 200) {
      final Map<String, dynamic> body = json.decode(response.body);
      final List<dynamic> data = body['data'] ?? [];
      return data.map((item) => AppNotification.fromJson(item)).toList();
    } else {
      throw Exception('Failed to load notifications (${response.statusCode})');
    }
  }

  /// Fetch unread notification count (GET /api/notifications/unread-count)
  Future<int> fetchUnreadCount() async {
    final response = await http.get(
      Uri.parse('$_baseUrl/notifications/unread-count'),
      headers: _headers,
    );

    if (response.statusCode == 200) {
      final Map<String, dynamic> body = json.decode(response.body);
      return body['unread_count'] as int? ?? 0;
    } else {
      throw Exception('Failed to load unread count (${response.statusCode})');
    }
  }

  /// Mark a single notification as read (PUT /api/notifications/:id/read)
  Future<bool> markNotificationAsRead(int id) async {
    final response = await http.put(
      Uri.parse('$_baseUrl/notifications/$id/read'),
      headers: _jsonHeaders,
    );

    if (response.statusCode == 200) {
      return true;
    } else {
      throw Exception('Failed to mark as read (${response.statusCode})');
    }
  }

  /// Mark all notifications as read (PUT /api/notifications/read-all)
  Future<bool> markAllNotificationsAsRead() async {
    final response = await http.put(
      Uri.parse('$_baseUrl/notifications/read-all'),
      headers: _jsonHeaders,
    );

    if (response.statusCode == 200) {
      return true;
    } else {
      throw Exception('Failed to mark all as read (${response.statusCode})');
    }
  }

  /// Delete a single notification (DELETE /api/notifications/:id)
  Future<bool> deleteNotification(int id) async {
    final response = await http.delete(
      Uri.parse('$_baseUrl/notifications/$id'),
      headers: _headers,
    );

    if (response.statusCode == 200) {
      return true;
    } else {
      throw Exception('Failed to delete notification (${response.statusCode})');
    }
  }

  /// Delete all notifications (DELETE /api/notifications)
  Future<bool> deleteAllNotifications() async {
    final response = await http.delete(
      Uri.parse('$_baseUrl/notifications'),
      headers: _headers,
    );

    if (response.statusCode == 200) {
      return true;
    } else {
      throw Exception(
        'Failed to delete notifications (${response.statusCode})',
      );
    }
  }
}
